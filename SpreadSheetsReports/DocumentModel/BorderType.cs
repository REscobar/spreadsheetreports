namespace SpreadSheetsReports.DocumentModel
{
    /// <summary>
    /// Enumerates the border types
    /// </summary>
    public enum BorderType : byte
    {
        /// <summary>
        /// No border
        /// </summary>
        None = 0,

        /// <summary>
        /// Thin border
        /// </summary>
        Thin = 1,

        /// <summary>
        /// Medium border
        /// </summary>
        Medium = 2,

        /// <summary>
        /// Dashed border
        /// </summary>
        Dashed = 3,

        /// <summary>
        /// Dotted border
        /// </summary>
        Dotted = 4,

        /// <summary>
        /// Thick border
        /// </summary>
        Thick = 5,

        /// <summary>
        /// Double border
        /// </summary>
        Double = 6,

        /// <summary>
        /// Hair border
        /// </summary>
        Hair = 7,

        /// <summary>
        /// Medium dashed border
        /// </summary>
        MediumDashed = 8,

        /// <summary>
        /// Dash dot border
        /// </summary>
        DashDot = 9,

        /// <summary>
        /// Medium dash dot border
        /// </summary>
        MediumDashDot = 10,

        /// <summary>
        /// Dash dot dot border
        /// </summary>
        DashDotDot = 11,

        /// <summary>
        /// Medium dash dot dot border
        /// </summary>
        MediumDashDotDot = 12,

        /// <summary>
        /// Salted dash dot border
        /// </summary>
        SlantedDashDot = 13
    }
}